#include <iostream>
using namespace std;
int main(){
  //operators in c++
  int a = 10,b =5;
  cout << "Sum = " << (a+b) << endl;
  cout << "Difference = " << (a-b) << endl;
  cout << "Product = " << (a*b) << endl;
  cout << "Division = " << (a/b) << endl;
  cout << "Modulo = " << (a%b) << endl;//modulo operator gives the remainder of the division is 0
  
  return 0;
}
